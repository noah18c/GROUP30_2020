
/**
 * Implementation of a class to return a boolean variable (out) and an array of Boolean variables (A_values) in Branching.java's methods
 *
 * @author Enrique Hortal
 * @version 1.0
 */

public class ReturnObject 
	{
	  public boolean out;
	  public Boolean[] A_values;

	  public ReturnObject (boolean out, Boolean[] A_values) 
	  {
	    this.out = out;
	    this.A_values = A_values;
	  }
}
